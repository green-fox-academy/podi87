public class Cuboid {
  public static void main(String[] args) {
    double aside = 10;
    double bside = 10;
    double cside = 10;
    double area = (6 * (aside * aside));
    double volume = (aside * bside * cside);
    System.out.println("Area: " + area);
    System.out.println("Volume: " + volume);
  }
}

